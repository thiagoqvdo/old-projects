    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed
        try {
            // TODO add your handling code here:
            editoraVO eVO = new editoraVO();
            EditoraDAO eDAO = new EditoraDAO();
            jfEditora jfEdi = new jfEditora();
            eVO.setIdEditora(this.idEditora);
            eVO.setNomeEditora(jtfNome.getText());
            eVO.setGerente(jtfGerente.getText());
            eVO.setEndereco(jtfEndereco.getText());
            eVO.setTelefone(jtfTelefone.getText());
            eDAO.alterarEditora(eVO);
            jfEdi.addRowToTableNew();
            JOptionPane.showMessageDialog(rootPane, "Editora alterada com sucesso!");
        } catch (SQLException ex) {
            Logger.getLogger(jfEditaEditora.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jbSalvarActionPerformed
