    private void jbConverter2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbConverter2ActionPerformed
        // TODO add your handling code here:
        if (!jtfValor.getText().equals("")) {
            double result = 0;
            double valor = Double.parseDouble(jtfValor.getText());

            if (jrdbKm2m.isSelected()) {
                result = valor / 1.6;
            } else if (jrdbM2Km.isSelected()) {
                result = valor * 1.6;
            }
            JOptionPane.showMessageDialog(this, "O resulado Ã©: "
                + NumberFormat.getInstance().format(result),
                "ConversÃ£o", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Valor nÃ£o pode ser vazio");
            jtfValor.requestFocus();
        }
    }//GEN-LAST:event_jbConverter2ActionPerformed

