    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed
        // TODO add your handling code here:
        livro l = new livro();
        byte i = 1;
        String erros[] = new String[6];
        boolean validaEditora = false, validaTitulo = false, validaAutor = false, validaIsbn = false, validaAssunto = false, validaQntd = false, validaPreco = false;

        if (!jtfTitulo.getText().equals("")) {
            l.setTitulo(jtfTitulo.getText());
            validaTitulo = true;
        } else {
            erros[i] = "Título não pode ser em branco";
            i++;
        }

        if (!jtfAutor.getText().equals("")) {
            l.setAutor(jtfAutor.getText());
            validaAutor = true;
        } else {
            erros[i] = "Autor não pode ser em branco";
            i++;
        }

        if (!jtfAssunto.getText().equals("")) {
            l.setAssunto(jtfAssunto.getText());
            validaAssunto = true;
        } else {
            erros[i] = "Assunto não pode ser em branco";
            i++;
        }

        if (!jtfIsbn.getText().equals("")) {
            l.setIsbn(jtfIsbn.getText());
            validaIsbn = true;
        } else {
            erros[i] = "ISBN não pode ser em branco";
            i++;
        }

        if (!jtfQntd.getText().equals("")) {
            l.setEstoque(Integer.parseInt(jtfQntd.getText()));
            validaQntd = true;
        } else {
            erros[i] = "Quantidade não pode ser em branco";
            i++;
        }

        if (!jtfPreco.getText().equals("")) {
            l.setPreco(Float.parseFloat(jtfPreco.getText()));
            validaPreco = true;
        } else {
            erros[i] = "Preço não pode ser em branco";
            i++;
        }

        if (!jcbEditora.getSelectedItem().toString().equals("Selecione")) {
            l.setIdLivro(cadEdi.addEditoraCB(jcbEditora.getSelectedItem().toString()));
            validaEditora = true;
            if (validaTitulo && validaAutor && validaAssunto && validaIsbn && validaPreco && validaQntd && validaEditora) {
                cadLiv.addLivro(l);
                JOptionPane.showMessageDialog(this, "Livro cadastrado com sucesso!");
                
            }
        } else {
            JOptionPane.showMessageDialog(this, "Primeiro selecione a editora", "Erro", ERROR);
        }


    }//GEN-LAST:event_jbSalvarActionPerformed

