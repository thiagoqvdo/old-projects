    public void addRowToTable() {
        DefaultTableModel model = (DefaultTableModel) jtLivros.getModel();

        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        Object rowData[] = new Object[7];//define vetor das colunas

        for (livro listLiv : cadLiv.getLivros()) {
            rowData[0] = listLiv.getTitulo();
            rowData[1] = listLiv.getIsbn();
            rowData[2] = listLiv.getAutor();
            rowData[3] = listLiv.getAssunto();
            rowData[4] = listLiv.getEstoque();
            rowData[5] = cadEdi.pesqNomeEditora(listLiv.getIdEditora());
            rowData[6] = listLiv.getPreco();

            model.addRow(rowData);

        }
    }


